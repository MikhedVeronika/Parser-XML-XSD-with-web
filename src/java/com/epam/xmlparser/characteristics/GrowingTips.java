package com.epam.xmlparser.characteristics;


public class GrowingTips {
    private int requiredTemperature;
    private String lightening;
    private int weekWateringMl;

    public int getRequiredTemperature() {
        return requiredTemperature;
    }

    public void setRequiredTemperature(int requiredTemperature) {
        this.requiredTemperature = requiredTemperature;
    }

    public String getLightening() {
        return lightening;
    }

    public void setLightening(String lightening) {
        this.lightening = lightening;
    }

    public int getWeekWateringMl() {
        return weekWateringMl;
    }

    public void setWeekWateringMl(int weekWateringMl) {
        this.weekWateringMl = weekWateringMl;
    }
}